    /**
     * 审核
     */
    @RequestMapping("/shBatch")
    @Transactional
    @SysLog("审核养殖计划")
    public R update(@RequestBody Long[] ids, @RequestParam String sfsh, @RequestParam String shhf){
        List<YangzhijihuaEntity> list = new ArrayList<YangzhijihuaEntity>();
        for(Long id : ids) {
            YangzhijihuaEntity yangzhijihua = yangzhijihuaService.selectById(id);
            yangzhijihua.setSfsh(sfsh);
            yangzhijihua.setShhf(shhf);
            list.add(yangzhijihua);
        }
        yangzhijihuaService.updateBatchById(list);
        return R.ok();
    }


    

